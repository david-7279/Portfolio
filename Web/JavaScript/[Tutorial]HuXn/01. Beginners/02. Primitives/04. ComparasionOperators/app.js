/* 
Relational Operators
  > Greater Then
  < Less Then
  >= Greater Then or equal to
  <= Less Then or equal to
*/

console.log(10 > 10);
console.log(10 < 10);
console.log(10 >= 10);
console.log(10 <= 10);


// ***********************************
/*
Equality Operators
  === strict equality (Type + Value)
  !== strict non-equality (Type + Value)
  == Lose equality (values)
  != Lose equality (values)
*/

console.log(10 === 10);
console.log(10 !== 10);
console.log(10 == 10);
console.log(10 != 10);